export function searchCommonFoods(query: string, filterCategory?: string): CommonFood[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const pool = filterCategory
    ? commonFoods.filter(f => f.searchCategories.includes(filterCategory as any))
    : commonFoods;

  const scored: { food: CommonFood; score: number }[] = [];

  for (const food of pool) {
    let score = 0;
    const nameLower = food.name.toLowerCase();

    // Exact name match
    if (nameLower === q) { score += 100; }
    // Name starts with query
    else if (nameLower.startsWith(q)) { score += 50; }
    // Name contains query
    else if (nameLower.includes(q)) { score += 30; }

    // Alias exact match
    if (food.aliases.some(a => a === q)) { score += 80; }
    // Alias starts with query
    else if (food.aliases.some(a => a.startsWith(q))) { score += 40; }
    // Alias contains query
    else if (food.aliases.some(a => a.includes(q))) { score += 20; }
    // Query contains alias (e.g., "scrambled egg" contains alias "egg")
    else if (food.aliases.some(a => q.includes(a) && a.length >= 3)) { score += 15; }

    if (score > 0) {
      // Prefer shorter names (more specific) as tiebreaker
      score += Math.max(0, 10 - food.name.length / 5);
      scored.push({ food, score });
    }
  }

  scored.sort((a, b) => b.score - a.score);
  return scored.slice(0, 10).map(s => s.food);
}

export function parseQueryQuantity(query: string): {
  cleanQuery: string;
  quantity?: number;
  grams?: number;
  oz?: number;
} {
  let cleanQuery = query;
  let quantity: number | undefined;
  let grams: number | undefined;
  let oz: number | undefined;

  // Match "Xg" or "X grams" patterns
  const gramMatch = query.match(/(\d+\.?\d*)\s*(?:g|grams?)\b\s*(.*)/i);
  if (gramMatch) {
    grams = parseFloat(gramMatch[1]);
    cleanQuery = gramMatch[2] || query.replace(gramMatch[0], "").trim();
    return { cleanQuery: cleanQuery || query.replace(/\d+\.?\d*\s*(?:g|grams?)/i, "").trim(), grams };
  }

  // Match "Xoz" or "X ounces"
  const ozMatch = query.match(/(\d+\.?\d*)\s*(?:oz|ounces?)\b\s*(.*)/i);
  if (ozMatch) {
    oz = parseFloat(ozMatch[1]);
    cleanQuery = ozMatch[2] || query.replace(ozMatch[0], "").trim();
    return { cleanQuery: cleanQuery || query.replace(/\d+\.?\d*\s*(?:oz|ounces?)/i, "").trim(), oz };
  }

  // Match "X lbs"
  const lbsMatch = query.match(/(\d+\.?\d*)\s*(?:lbs?|pounds?)\b\s*(.*)/i);
  if (lbsMatch) {
    oz = parseFloat(lbsMatch[1]) * 16;
    cleanQuery = lbsMatch[2] || query.replace(lbsMatch[0], "").trim();
    return { cleanQuery: cleanQuery || query.replace(/\d+\.?\d*\s*(?:lbs?|pounds?)/i, "").trim(), oz };
  }

  // Match count patterns like "2 eggs", "3 chicken breast"
  const countMatch = query.match(/^(\d+\.?\d*)\s+(.+)/);
  if (countMatch) {
    quantity = parseFloat(countMatch[1]);
    cleanQuery = countMatch[2];
  }

  return { cleanQuery: cleanQuery.trim() || query, quantity, grams, oz };
}
